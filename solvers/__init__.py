# CTF Solvers Package
# Available modules:
#   reverse   - 逆向工程 (PE/ELF分析、XOR爆破、字符串提取)
#   crypto    - 密码学 (编码识别、古典密码、MD5反查、多层解码)
#   data_sec  - 数据安全 (Luhn校验、数据清洗、数据脱敏)
#   archive   - 压缩包 (ZIP分析、密码爆破、伪加密检测)
#   stego     - 隐写分析 (LSB、binwalk、GIF/PNG分析、音频隐写)
#   traffic   - 流量分析 (pcap解析、隧道检测、USB键盘)
#   web       - Web安全 (SQLi、XSS、SSRF、XXE、命令注入)
#   firewall  - 防火墙 (iptables/nftables/Windows规则)
#   common    - 公共工具 (flag搜索、字符串提取)
